alias flist="flatpak list"                              # list installed apps and/or runtimes
alias finstall="flatpak install"                        # install an application or runtime
alias fremove="flatpak uninstall"                       # remove an installed application or runtime
alias fsearch="flatpak search"                          # search for remote apps/runtimes
alias fupdate="flatpak update"                          # update all your installed applications and runtimes
alias fremotes="flatpak remotes"                        # list the remotes